//Multiplication of Twon Numbers
function mulTwoNumber(startNumber, endNumber) {
    var mulResult = startNumber * endNumber;
    return mulResult;
}
var number1 = 10;
var number2 = 15;
var multiplicationResult = mulTwoNumber(number2, number1);
console.log('Multiplication of Two Numbers is : ', multiplicationResult);


